        //
        // POST: /Account/Login
        [HttpPost]
        [AllowAnonymous]
        [ValidateAntiForgeryToken]
        public async Task<ActionResult> Login(LoginViewModel model, string returnUrl)
        {
            if (!ModelState.IsValid)
            {
                return View(model);
            }

            // This doesn't count login failures towards account lockout
            // To enable password failures to trigger account lockout, change to shouldLockout: true
            var result = await SignInManager.PasswordSignInAsync(model.Username, model.Password, model.RememberMe, shouldLockout: false);

            var currentUser = await UserManager.FindByNameAsync(model.Username);
            // Grab token for use of API for Admin and Supervisor users
            if (result == SignInStatus.Success && (UserManager.IsInRole(currentUser.Id, UserRankUtil.ADMIN) || UserManager.IsInRole(currentUser.Id, UserRankUtil.SUPERVISOR)))
            {
                var baseAPIAddress = System.Web.Configuration.WebConfigurationManager.AppSettings["APIBaseURL"];
                var client = new HttpClient()
                {
                    BaseAddress = new Uri(baseAPIAddress)
                };

                var content = new FormUrlEncodedContent(new[]
                        {
                    new KeyValuePair<string, string>("grant_type", "password"),
                    new KeyValuePair<string, string>("username", model.Username),
                    new KeyValuePair<string, string>("password", model.Password)
                });

                var resultAuth = await client.PostAsync("/Token", content);
                string resultContent = await resultAuth.Content.ReadAsStringAsync();
                resultContent = resultContent.Replace(".issued", "issued").Replace(".expires", "expires");

                AuthToken tokenResponse = JsonConvert.DeserializeObject<AuthToken>(resultContent);
                tokenResponse.UserID = currentUser.Id;
                tokenResponse.Active = true;
                _employeeLogic.SaveToken(tokenResponse);
                _employeeLogic.MarkTokensAsInactive(currentUser.Id);                                
            }

            switch (result)
            {
                case SignInStatus.Success:
                    return RedirectToLocal(returnUrl);
                case SignInStatus.LockedOut:
                    return View("Lockout");
                case SignInStatus.RequiresVerification:
                    return RedirectToAction("SendCode", new { ReturnUrl = returnUrl, RememberMe = model.RememberMe });
                case SignInStatus.Failure:
                default:
                    ModelState.AddModelError("", "Invalid login attempt.");
                    return View(model);
            }
        }                  
               
